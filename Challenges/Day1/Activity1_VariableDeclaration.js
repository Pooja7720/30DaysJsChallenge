// TASK 1: DECLARE A VARIABLE USING var, ASSIGN IT A NUMBER, AND LOG THE VALUE TO THE CONSOLE
var num = 48;
console.log("The value in num variable is",num);

// TASK 2: DECLARE A VARIABLE USING let, ASSIGN IT A STRING, AND LOG THE VALUE TO THE CONSOLE
let string = "This is the first javascript string";
console.log("Value in string variable: ",string);

// OUTPUT
/*
$ node Activity1_VariableDeclaration.js
The value in num variable is 48
Value in string variable:  This is the first javascript string
*/